/* 동적 언어 dynamic language
	: 변수를 정의할 때 자료형 type 을 정의할 필요가 없다 */

let userId; // 문자일까 숫자일까

userId = 12; // 숫자를 할당
console.log(userId, typeof userId);

userId = "simon"; // 문자열을 할당
console.log(userId, typeof userId);

